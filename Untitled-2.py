# Enunciado:
# Escribe un programa que imprima los números del 1 al 5 usando un ciclo for,
# y luego imprima los números pares del 2 al 10 usando un ciclo while.

# Imprimir números del 1 al 5 con for
for i in range(1, 6):
    print(i)

# Imprimir números pares del 2 al 10 con while
j = 2
while j <= 10:
    print(j)
    j += 2


